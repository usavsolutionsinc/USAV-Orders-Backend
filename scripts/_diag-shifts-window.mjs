import pg from 'pg';
const client = new pg.Client({ connectionString: process.env.DATABASE_URL, ssl: { rejectUnauthorized: false } });
await client.connect();
const r = await client.query(`SELECT COUNT(*) AS n FROM shifts WHERE starts_at >= '2026-05-11' AND starts_at < '2026-05-16'`);
const h = await client.query(`SELECT id, name, shifts_materialized_through FROM staff WHERE active = true ORDER BY id LIMIT 3`);
const test = await client.query(`SELECT materialize_shifts(1, '2026-05-11'::date, '2026-05-15'::date) AS created`);
const r2 = await client.query(`SELECT COUNT(*) AS n FROM shifts WHERE starts_at >= '2026-05-11' AND starts_at < '2026-05-16'`);
const templates = await client.query(`SELECT id, staff_id, day_of_week, effective_from FROM shift_templates WHERE staff_id = 1 ORDER BY day_of_week`);
console.log('shifts in 5/11-5/15 before:', r.rows[0].n);
