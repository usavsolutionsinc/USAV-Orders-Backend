#!/usr/bin/env node
/**
 * Dead Code Report Generator
 *
 * Usage:
 *   node scripts/generate-dead-code-report.mjs
 *   node scripts/generate-dead-code-report.mjs --json-only
 *
 * Produces:
 *   - reports/dead-code-YYYY-MM-DD.md (human readable)
 *   - reports/knip-report.json (machine readable)
 *
 * Part of the Dead Code Cleanup Plan — see docs/DEAD_CODE_CLEANUP_PLAN.md
 */

import { execSync } from 'node:child_process';
import { mkdirSync, writeFileSync, existsSync } from 'node:fs';
import { join } from 'node:path';

const date = new Date().toISOString().slice(0, 10);
const reportsDir = 'reports';

if (!existsSync(reportsDir)) {
  mkdirSync(reportsDir, { recursive: true });
}

const jsonReportPath = join(reportsDir, `knip-report-${date}.json`);
const mdReportPath = join(reportsDir, `dead-code-${date}.md`);

function run(cmd, opts = {}) {
  try {
    return execSync(cmd, { encoding: 'utf8', stdio: opts.silent ? 'pipe' : 'inherit', ...opts });
  } catch (e) {
    if (opts.ignoreError) return e.stdout || e.stderr || '';
    throw e;
  }
}

console.log('=== Dead Code Report Generator ===');
console.log(`Date: ${date}\n`);

let knipOutput = '';
let knipJson = null;

try {
  console.log('→ Running knip (compact + json)...');
  knipOutput = run('npx knip --reporter compact', { silent: true, ignoreError: true });
  const jsonRaw = run('npx knip --reporter json', { silent: true, ignoreError: true });
  try {
    knipJson = JSON.parse(jsonRaw);
    writeFileSync(jsonReportPath, JSON.stringify(knipJson, null, 2));
    console.log(`   ✓ Wrote ${jsonReportPath}`);
  } catch {
    console.warn('   ! Could not parse knip JSON output');
  }
} catch (e) {
  console.warn('   ! knip run failed or produced no output');
  knipOutput = String(e.stdout || e.stderr || e);
}

let depcruiseOutput = '';
try {
  console.log('→ Running dependency-cruiser (orphans focus)...');
  depcruiseOutput = run(
    'npx depcruise src --config .dependency-cruiser.cjs --output-type text 2>&1 | head -80',
    { silent: true, ignoreError: true }
  );
} catch (e) {
  depcruiseOutput = String(e);
}

// Root junk scan (simple heuristic)
const rootJunk = [];
const suspiciousRoot = [
  'firebase-debug.log',
  'receiving_lines_cleanup_backup',
  'Repair Service HTML',
  'get-all-ebay-tokens.js',
  'get-ebay-tokens.js',
  'test-packing-flow.sh',
];
for (const name of suspiciousRoot) {
  if (existsSync(name)) rootJunk.push(name);
}

// Build markdown report
const md = `# Dead Code Report — ${date}

Generated by \`scripts/generate-dead-code-report.mjs\`

See [docs/DEAD_CODE_CLEANUP_PLAN.md](../docs/DEAD_CODE_CLEANUP_PLAN.md) for the full plan and triage process.

## Summary

- **Knip unused files reported**: ${knipJson?.files?.length ?? 'unknown (see raw output)'}
- **Root junk detected**: ${rootJunk.length} items
- **Dependency-cruiser** output captured below (orphans + warnings)

## Root-Level Junk (quick wins)

${rootJunk.length > 0 ? rootJunk.map((f) => `- ${f}`).join('\n') : '_None detected by this run._'}

**Recommended action**: Use 'git rm' for already-committed files + ensure they stay in .gitignore (see Phase 1 of the plan).

## Knip Compact Output (top of report)

\`\`\`
${knipOutput.split('\n').slice(0, 120).join('\n')}
\`\`\`

${knipJson ? `Full machine-readable report saved to \`${jsonReportPath}\`.` : ''}

## Dependency-Cruiser (orphans & issues)

\`\`\`
${depcruiseOutput}
\`\`\`

## Next Steps

1. Review the full knip JSON for the complete list of 100+ candidates.
2. Follow the triage process in \`docs/DEAD_CODE_CLEANUP_PLAN.md\` (Phase 3).
3. Record decisions in \`docs/dead-code-triage.md\`.
4. Run verification before any deletion:
   \`\`\`bash
   npm run build
   npm run lint
   npx tsc --noEmit
   \`\`\`

## Raw Knip JSON Location

\`${jsonReportPath}\`

---

*Report generated on ${date} — re-run the script after any config or code changes.*
`;

writeFileSync(mdReportPath, md);
console.log(`\n✓ Wrote ${mdReportPath}`);
console.log('\nDone. Open the markdown report for a human-friendly summary.');
console.log('For the living triage list, edit docs/dead-code-triage.md');
